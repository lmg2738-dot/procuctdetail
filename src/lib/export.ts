export function generateMarkdown(content: GeneratedContent): string {
  const features = (content.features ?? []).map((f) => `- ${f}`).join("\n");
  const faq = (content.faq ?? [])
    .map((item) => `**Q: ${item.question}**\nA: ${item.answer}`)
    .join("\n\n");
  const keywords = (content.seoKeywords ?? []).map((kw) => `#${kw}`).join(" ");

  return `# ${content.title ?? "상품명"}

> ${content.thumbnailText}

## 상품 설명

${content.description}

## 광고 문구

> ${content.marketingCopy}

## 상품 장점

${features}

## FAQ

${faq}

## SEO 키워드

${keywords}
`;
}

export function downloadFile(
  content: string,
  filename: string,
  mimeType: string
): void {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
