import type { GeneratedContent, FAQItem } from "@/types";

function escapeHtml(text: string | null | undefined): string {
  const value = text ?? "";
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function renderFaqHtml(faq: FAQItem[]): string {
  return faq
    .map(
      (item) => `
    <div class="faq-item">
      <h3 class="faq-question">${escapeHtml(item.question)}</h3>
      <p class="faq-answer">${escapeHtml(item.answer)}</p>
    </div>`
    )
    .join("\n");
}

function renderFeaturesHtml(features: string[]): string {
  return features
    .map((f) => `<li class="feature-item">${escapeHtml(f)}</li>`)
    .join("\n");
}

